package uc.seng301.cardbattler.asg4.game;

/**
 * Play state enum representing different stages of an action being executed
 */
public enum PlayState {
    /**
     * Represents the before play state of an action
     */
    BEFORE_PLAY,
    /**
     * Represents the on play state of an action
     */
    ON_PLAY,
    /**
     * Represents the after play state of an action
     */
    AFTER_PLAY
}
